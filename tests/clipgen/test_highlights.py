"""ハイライト検出のテスト."""
from __future__ import annotations

import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "src"))

from clipgen.highlights import detect_highlights, parse_srt  # noqa: E402


SAMPLE_SRT = (ROOT / "src" / "clipgen" / "data" / "fixtures" / "sample.srt").read_text(encoding="utf-8")


def test_parse_srt_basic():
    cues = parse_srt(SAMPLE_SRT)
    assert len(cues) >= 10
    assert cues[0].start_sec < cues[1].start_sec
    assert "予算委員会" in cues[0].text


def test_parse_webvtt_header_note_and_cue_settings():
    text = """WEBVTT

NOTE generated by test
this comment should be ignored

cue-1
00:00:01.000 --> 00:00:05.000 align:start position:50%
これは絶句の場面です。

00:00:06.000 --> 00:00:09.000
次の発言です。
"""
    cues = parse_srt(text)

    assert len(cues) == 2
    assert cues[0].start_sec == 1.0
    assert cues[0].end_sec == 5.0
    assert "絶句" in cues[0].text
    assert "NOTE" not in cues[0].text


def test_parse_srt_keeps_emoji_and_symbols():
    text = """WEBVTT

00:00:01.000 --> 00:00:05.000
これはすごいです😮‼ 絶句しました。
"""
    cues = parse_srt(text)

    assert len(cues) == 1
    assert "😮" in cues[0].text
    assert "‼" in cues[0].text


def test_detect_short_highlight_is_capped_at_60s():
    cues = parse_srt(SAMPLE_SRT)
    hs = detect_highlights(cues, target_format="short")
    assert len(hs) == 1
    h = hs[0]
    assert h.duration_sec <= 60.0
    assert any(kw in h.keywords for kw in ("完全論破", "絶句", "失言"))


def test_detect_long_highlights_have_multiple_windows():
    cues = parse_srt(SAMPLE_SRT)
    hs = detect_highlights(cues, target_format="long", long_total_target_sec=300, max_long_windows=5)
    assert len(hs) >= 1
    # 並び順は start_sec 昇順
    for a, b in zip(hs, hs[1:]):
        assert a.start_sec <= b.start_sec


def test_detect_long_highlights_respects_min_total_sec():
    srt = """1
00:00:00,000 --> 00:00:40,000
絶句の答弁です。

2
00:00:50,000 --> 00:01:30,000
論破の場面です。

3
00:01:40,000 --> 00:02:20,000
失言について話します。
"""
    cues = parse_srt(srt)
    hs = detect_highlights(
        cues,
        target_format="long",
        long_window_sec=40,
        max_long_windows=1,
        min_total_sec=100,
    )

    assert sum(h.duration_sec for h in hs) >= 100


def test_detect_highlights_empty():
    assert detect_highlights([], target_format="short") == []


def test_detect_highlights_low_score_returns_empty():
    cues = parse_srt("1\n00:00:00,000 --> 00:00:05,000\nおはようございます。\n")
    assert detect_highlights(cues, target_format="short") == []


def test_invalid_target_raises():
    import pytest

    with pytest.raises(ValueError):
        detect_highlights([], target_format="medium")
